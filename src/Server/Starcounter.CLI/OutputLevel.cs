
namespace Starcounter.CLI {
    /// <summary>
    /// Defines possible output levels (i.e "verbosity") of a given CLI tool.
    /// </summary>
    public enum OutputLevel {
        /// <summary>
        /// Minimal output.
        /// </summary>
        Minimal = 0,
        /// <summary>
        /// Normal output.
        /// </summary>
        Normal = 1,
        /// <summary>
        /// Verbose (detailed) output
        /// </summary>
        Verbose = 2
    }
}
